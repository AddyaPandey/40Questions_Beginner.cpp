#include<iostream>
using namespace std;
int main(){

int x = 4;
cout<<x <<endl;
cout<<x++ <<endl; //post increment
cout<<x <<endl <<endl; 

x = 4;
cout<<x <<endl;
cout<<++x <<endl; //pre increment
cout<<x <<endl <<endl;

x = 4;
cout<<x <<endl;
cout<<x-- <<endl; //post decrement
cout<<x <<endl <<endl; 

x = 4;
cout<<x <<endl;
cout<<--x <<endl; //pre decrement
cout<<x;
}